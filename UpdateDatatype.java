package basics;

public class UpdateDatatype {

	public static void main(String[] args) {
		int a = 100;
		System.out.println(a);
		a = 101; //Update DataType
		System.out.println(a);
		
		String name = "Pranali";
		System.out.println(name);
		name = "Pranali Kaswa";
		System.out.println(name);
	}
}
